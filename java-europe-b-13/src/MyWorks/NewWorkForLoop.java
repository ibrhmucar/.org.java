package MyWorks;

public class NewWorkForLoop {

	public static void main(String[] args) {
		
		
	for (int i=1 ; i<=7 ; i++) {
			
			for (int k=1 ; k<i ; k++) {
				System.out.print(" ");
			}
			for (int j=7 ; j>=i ; j--) {
			
			System.out.print(j);
			System.out.print(" ");
		}
			
			System.out.println();
		
	}
		 
	for (int i=1 ; i<=6 ; i++) {
		
		   for(int j=1 ; j<=i+1 ; j++){
			   
			   System.out.print(j);
		   }
		   System.out.println();
	}
	
		
		
	}

}
