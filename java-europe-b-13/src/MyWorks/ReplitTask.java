package MyWorks;
import java.util.Scanner;
public class ReplitTask {

	public static void main(String[] args) {
	  
		
	for (int i=1 ; i<=1 ; i++) {
		for (int j=1 ; j<=5 ; j++) {
			System.out.print("*");
		}
			System.out.println();
	}
		for (int i=1 ; i<=3 ; i++) {
			for (int j=1 ; j<=5 ; j++) {
				if (j==2 || j==3 || j==4) {
					System.out.print(" ");
				}else {
					System.out.print("*");
				}
				
		}
			System.out.println();
		
	}
		
		for (int i=1 ; i<=1 ; i++) {
			for (int j=1 ; j<=5 ; j++) {
				System.out.print("*");
			}
				System.out.println();
		
		
		}		
}
}	