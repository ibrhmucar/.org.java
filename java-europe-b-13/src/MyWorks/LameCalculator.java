package MyWorks;

public class LameCalculator {

	
	


public static int plus(int a, int b){

  int sum = a+b;
  return sum;
}
public static int minus(int a, int b){

  int sum = a-b;
  return sum;
}
public static int multiply(int a, int b){

  int sum = a*b;
  return sum;
}
public static int divide(int a, int b){

  int sum = a/b;
  return sum; 
}	
	
}
